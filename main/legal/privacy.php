	<h3 class="cssImg" id="privacy_introduction"><span class="img_t_privacy_introduction">1. Introduction</span></h3>

<p>Symbian Foundation Limited (referred to as "Symbian Foundation", "we, or "our") is committed to protecting the privacy of visitors to the Symbian Foundation websites (including but not limited to the websites at the following uniform resource locators: <a href="http://www.symbian.org" target="_blank">http://www.symbian.org</a> and <a href="http://developer.symbian.org">http://developer.symbian.org</a> together with all related discussion forums, directories and databases). We have put in place policies and procedures to protect personal information we collect about you in order to comply with the Data Protection Act 1998. This privacy policy explains why we collect information about you and what we do with it. If you do not agree with this privacy policy, please do not use this website. By accessing and using this website, you agree to the terms of this privacy policy. </p>

<h3 class="cssImg" id="privacy_information"><span class="img_t_privacy_information">2. Why do we collect certain information about you?</span></h3>


<p class="listHeader">We collect and process certain personal information about you in order to:</p>
<ul style="margin-left:20px">
<li>register you for certain services available from our websites;</li>
<li>enable you to make transactions on our websites;</li>
<li>provide you with news alerts or information about Symbian Foundation events or to enable you to take part in surveys if you wish to;</li>
<li>market and promote our services or the services of selected third parties to you. We will only do this with your consent;</li>
<li>enhance or improve your experience of our website by collecting details of your visit to our websites using cookies and standard weblogs;</li>
<li>administer our websites.</li>
</ul>

<h3 class="cssImg" id="privacy_retain"><span class="img_t_privacy_retain">3. How long do we retain your information?</span></h3>

<p>We only collect information which is necessary for the operation of our websites and for the provision of certain services available from our websites. We will not keep your personal information for longer than necessary to provide the services available from our websites or as required by law.</p>

<h3 class="cssImg" id="privacy_cookies"><span class="img_t_privacy_cookies">4. Use of cookies</span></h3>

<p>The use of cookies is common and many websites use them. A cookie is a small data storage program which is stored by your computer's web browser on your hard drive. Each time your web browser requests a page from our web server the message is sent back to your computer. The message enables the websites to speed up and simplify the logging on/purchasing process, record your activities and personalise the website for your use. Any information collected by cookies may be used to personalise the services available from our websites to you. We will only use cookies to view information on your computer that was put there by a cookie from our websites. The Help menu on the menu bar of most browsers will tell you how to prevent your browser from accepting new cookies, how to have the browser notify you when you receive a new cookie and how to disable cookies altogether. Further information on cookies and the removal of cookies can be found on the Information Advertising Bureau website. If you choose to disable cookies, your access to our websites may be limited.</p>

<h3 class="cssImg" id="privacy_acessing"><span class="img_t_privacy_accessing">5. Accessing and amending your personal information</span></h3>

<p>You may request a copy of all your personal information held by us by contacting the General Counsel, Legal Department, Symbian Foundation Limited, 1 Boundary Row, Southwark, London, SE1 8HP, UK. Foundation will charge a £10 administration fee for providing the copies.</p>

<p>We will amend any incorrect personal information held by us. We will remove your personal information from our records within 30 days of receiving your notification to do so, subject to any legal requirements to retain such information. Please notify the General Counsel, if you believe your personal information held by us is incorrect or you wish to remove your personal information from our records.</p>

<p>Where you have registered for a Service you may be required to create an online profile. You will be able to access and edit the information on your profile page though the applicable website. You will also be able to change the consents you have given us through the opt-in process.</p>


<h3 class="cssImg" id="privacy_disclosure"><span class="img_t_privacy_disclosure">6. Disclosure to Third Parties</span></h3>

<p>If you register to participate in a Symbian Foundation event via our websites your details may be disclosed to a third party service provider who shall organise, promote and run the Symbian Foundation event on our behalf. The third party service provider will not use your details for any other reason without your consent.</p>

<p>If you have given your consent, your information may also be disclosed to other participants in a Symbian Foundation event or an event Symbian Foundation participates in.</p>

<p>If you have given your consent, we may share your information with other selected third parties so they may contact you to let you know about products or services which may be of interest to you.</p>

<p>We may also provide sales, traffic and demographic statistics and other related information to third parties but these statistics will not contain any information which could be used to identify any individual person.</p>

<p>We reserve the right to access and disclose to third parties your personal information where necessary to comply with applicable laws and lawful authority requests, to safeguard the proper operation of our systems and to protect ourselves and our customers or in order to apply our terms of use of the Symbian Foundation web sites including but not limited to those listed in section 1 above.
</p>

<h3 class="cssImg" id="privacy_consent"><span class="img_t_privacy_consent">7. Consent/opt-ins</span></h3>

<p>If you submit personal information through the websites and tick the opt-in boxes set out on the relevant page, then you give consent to the use of the personal information by Symbian Foundation and/or other companies selected by us for the purposes described.</p>

<p>Where you choose to opt-in on our website, this will only apply to information stored about you on our website's databases. If you have provided information to Symbian Foundation through another channel, the use of that information will not be affected by the opt-ins you select on this website.</p>

<p>Where you have consented to such use of your information, we may send you this information by post, email and/or wireless communication.</p>

<h3 class="cssImg" id="privacy_transfer"><span class="img_t_privacy_transfer">8. Transfer of personal information outside the European Economic Area ("EEA")</span></h3>

<p>We will not transfer your personal information outside the EEA with the exception of transfer of information to Symbian Foundation offices outside the EEA, and/or (where you have given us your consent to do so) selected third parties, in relation to the provision of the services available from our websites.</p>

<h3 class="cssImg" id="privacy_protection"><span class="img_t_privacy_protection">9. Protection of personal information</span></h3>

<p>We use up to date industry procedures to secure your personal information held by us from unauthorised access, use, disclosure or loss. We may also protect the security of your personal information during transmission using Secure Sockets Layer ("SSL")/encryption software.</p>

<h3 class="cssImg" id="privacy_children"><span class="img_t_privacy_children">10. Children</span></h3>

<p>We do not specifically collect personal information about children and request that children obtain their parents' consent before providing personal information. We recommend that parents actively monitor their children's use of the Internet and our websites.</p>

<h3 class="cssImg" id="privacy_third_party"><span class="img_t_privacy_third_party">11. Third party websites</span></h3>

<p>This privacy policy only applies to our websites. Our websites may contain links to third party websites. Please note that we are not responsible for the privacy practices of other websites. We recommend that you check the privacy policy of any other websites you may visit through our websites.</p>

<h3 class="cssImg" id="privacy_changes"><span class="img_t_privacy_changes">12. Changes to our Privacy Policy</span></h3>
<p>We may amend this privacy policy from time to time. If we make any changes we will post them on our websites, so that you will always be aware of the way we use your personal information.</p>

